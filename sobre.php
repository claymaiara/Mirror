	<p>A fundação em 1932 ocorreu no momento da descoberta econônica do
		interior do Paraná. A família Pelho, tradicional da região, investiu
		todas as suas economias nessa nova iniciativa, revolucionária para a
		época. O fundador Eduardo Simões Pelho, dotado de particular visão
		administrativa, guiou os negócios da empresa durante mais de 50 anos,
		muitos deles ao lado de seu filho E. S. Pelho Filho, atual CEO. O nome
		da empresa é inspirado no nome da família.</p>
	<p>O crescimento da empresa foi praticamente instantâneo. Nos primeiros
		5 anos, já atendia 18 países. Bateu a marca de 100 países em apenas 15
		anos de existência. Até hoje, já atendeu 740 milhões de usuários
		diferentes, em bilhões de diferentes pedidos.</p>
	<p>O crescimento em número de funcionários é também assombroso. Hoje, é
		a maior empregadora do Brasil, mas mesmo após apenas 5 anos de sua
		existência, já possuía 30 mil funcionários. Fora do Brasil, há 240 mil
		funcionários, além dos 890 mil brasileiros nas instalações de
		Jacarezinho e nos escritórios em todo país.</p>
	<p>Dada a importância econômica da empresa para o Brasil, a família
		Pelho já recebeu diversos prêmios, homenagens e condecorações. Todos
		os presidentes do Brasil já visitaram as instalações da Mirror
		Fashion, além de presidentes da União Européia, Ásia e o
		secretário-geral da ONU.</p>
